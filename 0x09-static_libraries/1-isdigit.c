/**
 * _isdigit - checks if its a number or letter
 *@c: character to be checked
 * Return: 1 if its a number && 0 for otherwis
 */
int _isdigit(int c)
{
        if (c >= 48 && c <= 57)
        {
                return (1);
        }
        {
                return (0);
        }


}
