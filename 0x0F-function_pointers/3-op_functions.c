/**
 * op_add - returns sum of a and b
 * @a: first input
 * @b: second input
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returs the difference
 * @a: first input
 * @b: second input
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first input
 * @b: second input
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a / b
 * @a: first input
 * @b: second input
 * Return: result of the divisiom of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of division
 * @a: first input
 * @b: second input
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
